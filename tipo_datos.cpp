#include <iostream>
using namespace std;



int main(){
	// TIPO DE DATOS
    /*
    int - Entero - 0,1,2,3,-5, etc...
	float - Flotantes - 0.1,0.5,-7.1, etc...
	string - Cadenas de texto - Hola, mundo , etc...
	char - Caracteres - a,b,t,e,etc...
	bool/boolean - true, false
    */

	string saludo = "hola";
	int numero = 10;
	float decimal = 15.5;
	char caracter = 'a';
	bool b=true;
	cout<<"Este es un string: " <<saludo<<endl;
	cout<<"Este es un numero: "<<numero<<endl;
	cout<<"Este es un decimal: "<<decimal<<endl;
	cout<<"Este es un caracter: "<<caracter<<endl;
	cout<<"Este es un bool: "<<b<<endl;
	return 0;
}